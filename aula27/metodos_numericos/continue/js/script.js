//parseFloat: transforma em um número decimal.

console.log(parseFloat(12.34));

console.log(Number.parseFloat(12.34));


//parseInt: transforma em um número inteiro.

console.log(parseInt(15.345));

console.log(Number.parseInt(15.345));


//toFixed(x): controla o número de casas decimais.

console.log(15.3456.toFixed(2));